import javax.swing.*;
import java.awt.*;

public class juegosdisplay extends JPanel {
    private String title;
    private String description;

    public juegosdisplay(String title, String description) {
        this.title = title;
        this.description = description;

        setPreferredSize(new Dimension(320, 208));
        setLayout(null);  // Usamos un dise√±o nulo para posicionar los elementos manualmente
        setBackground(new Color(44, 47, 72));
        setBorder(BorderFactory.createRoundedBorder(20));

        JLabel titleLabel = new JLabel(title);
        titleLabel.setBounds(12, 113, 139, 20);
        titleLabel.setForeground(Color.WHITE);
        add(titleLabel);

        JTextArea descriptionArea = new JTextArea(description);
        descriptionArea.setBounds(12, 145, 283, 28);
        descriptionArea.setForeground(new Color(255, 255, 255, 55));
        descriptionArea.setOpaque(false);
        descriptionArea.setEditable(false);
        add(descriptionArea);
    }

    public static void main(String[] args) {
        JFrame frame = new JFrame("Featured Card Example");
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);

        juegosdisplay juego = new juegosdisplay("Your Title", "Your Description");
        frame.add(juego);

        frame.pack();
        frame.setLocationRelativeTo(null);
        frame.setVisible(true);
    }
}

